# Declaring variables
name = "Raghul"      # String
age = 22           # Integer
height = 5.9         # Float
is_student = True    # Boolean

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Assigning the same value
x = y = z = 50

print("x:", x)
print("y:", y)
print("z:", z)


# Assigning values to multiple variables
a, b, c = 10, 20, 30

print("a:", a)
print("b:", b)
print("c:", c)