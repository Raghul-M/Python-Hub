# Integer example
age = 25
print(age)  # Output: 25

# Float example
height = 5.9
print(height)  # Output: 5.9

# String example
name = "Raghul"
print(name)  # Output: Raghul

# List example
fruits = ["apple", "banana", "cherry"]
print(fruits)  # Output: ['apple', 'banana', 'cherry']

# Tuple example
coordinates = (10.5, 20.3, 30.7)
print(coordinates)  # Output: (10.5, 20.3, 30.7)

# Range example
numbers = range(1, 6)
print(list(numbers))  # Output: [1, 2, 3, 4, 5]

# Dictionary example
student = {"name": "Raghul", "age": 25, "course": "Python"}
print(student)  # Output: {'name': 'Raghul', 'age': 25, 'course': 'Python'}

# Boolean example
is_valid = True
print(is_valid)  # Output: True